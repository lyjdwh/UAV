/******
将得到的数据按照用户希望的格式构建成字符串
char *format , //输出的字符串格式
int data_1 , //输入的数据1
int data_2 , //输入的数据2
int x , //输出位置x
int y 	//输出位置y
******/
void ConstructeStr(
				   char *title ,
				   char *format , //输出的字符串格式
				   int data_1 , //输入的数据1
				   int data_2 , //输入的数据2
				   int x , //输出位置x
				   int y 	//输出位置y
				   )
{
	char *temp = format;
	char display[20] = "", loop = 0;
	char data_str_1[6] , data_str_2[6] ;
	char flag = 0;
	const int color = 0xffff;
	
	itoa(data_1 , data_str_1 , 10);
	itoa(data_2 , data_str_2 , 10);
	strcpy(display , "");

	PrintHZ16(x - 85,y,title,color,1,1);  
	
	while(*temp != '\0')
	{
		if(*temp != 'd')
		{
			display[loop] = *temp;
			loop++;
			temp++;
		}
		else 
		{
			flag++;
			switch(flag)
			{
				case 1:
					strcat(&display[loop] , data_str_1);
					temp++;
					loop += strlen(data_str_1);
					break;
				
				case 2:
					strcat(&display[loop] , data_str_2);
					temp++;
					loop += strlen(data_str_2);
					break;

				default :
					break;
			}
		}
	}
	display[loop] = '\0';
	DisplayData(x , y , display , color);
}
